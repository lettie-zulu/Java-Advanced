package Lesson4;

public class TestMathStaticMethod {
        public static void main(String[] args) {
            System.out.println("random number is  " + Math.random());
            System.out.println("square root is " + Math.sqrt(9.00));
            System.out.println("rounded random " + Math.round(Math.random()));
            System.out.println("absolute  " + Math.abs(9));
        }
    }
